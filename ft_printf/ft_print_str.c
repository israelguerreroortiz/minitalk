#include "ft_printf.h"

size_t	ft_print_str(char *s)
{
	size_t	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (s[i] != '\0')
	{
		ft_print_char(s[i]);
		i++;
	}
	return (i);
}
